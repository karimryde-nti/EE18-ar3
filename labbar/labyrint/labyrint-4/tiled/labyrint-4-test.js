// Rita kartan
function ritaKartan() {
    var index = 0;

    // Loopa igenom raderna
    for (var rad = 0; rad < 40; rad++) {

        // Loopa igenom kolumnerna
        for (var kolumn = 0; kolumn < 40; kolumn++) {

            // Plocka ut rätt ruta
            var x = Math.floor(karta[index] % 16 - 1) * 32;
            var y = Math.floor(karta[index] / 16) * 32;

            // Rita ut rutan
            ctx.drawImage(kartbild, x, y, 32, 32, kolumn * 32, rad * 32, 32, 32);

            // Hoppa till nästa ruta
            index++;
        }
    }
}